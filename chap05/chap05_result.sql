SELECT *
    FROM EMP   
    WHERE ENAME LIKE '%S';
    
--2번

SELECT EMPNO, ENAME, JOB, SAL, DEPTNO
    FROM EMP
    WHERE DEPTNO = '30'
      AND JOB = 'SALESMAN';
        
--3번

--집합 연산자 X
SELECT EMPNO, ENAME, JOB, SAL, DEPTNO
    FROM EMP
    WHERE DEPTNO IN(20, 30)
      AND SAL > 2000;
      
--집합 연산자 O
SELECT EMPNO, ENAME, JOB, SAL, DEPTNO
    FROM EMP
    WHERE DEPTNO = 20
      AND SAL > 2000
UNION ALL
SELECT EMPNO, ENAME, JOB, SAL, DEPTNO
    FROM EMP
    WHERE DEPTNO = 30
      AND SAL > 2000;
      
--4번 
--(NOT BETWEEN A AND B)
SELECT *    
    FROM EMP
    WHERE SAL NOT BETWEEN 2000 AND 3000;

--OR
SELECT *
    FROM EMP
    WHERE SAL < 2000 OR SAL > 3000;

--NOT AND    
SELECT *
    FROM EMP
    WHERE NOT (SAL >= 2000 AND SAL <= 3000);
    
--5번

SELECT ENAME, EMPNO, SAL, DEPTNO
    FROM EMP
    WHERE ENAME LIKE '%E%'
      AND DEPTNO = 30
      AND SAL NOT BETWEEN 1000 AND 2000;
      
--6번

SELECT *
    FROM EMP
    WHERE (COMM IS NULL OR COMM = '0')
      AND MGR IS NOT NULL
      AND JOB IN('MANAGER', 'CLERK')
      AND ENAME NOT LIKE '_L%';
      
      